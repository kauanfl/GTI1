package exercicio_181;

public class Exercicio_181 {

    public static void main(String[] args) {
        //Criar um algoritmo que imprima todos os numeros de 1 a 100 e a soma deles
        
        //Variaveis 
        int i;
        int soma = 0;
     
      
      //Processamento e saida
      for (i = 1; i <= 100; i++) {
        soma += i;
        System.out.println(soma);
        
      }
      
      
    }
    
}
